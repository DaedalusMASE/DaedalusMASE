"""
**sub_Heating_Sources.current_pedersen**

**Description**:
_____________________________________________________________________________________________________________________

Calculate Pedersen current density

$$J_{Pedersen}=\\sigma_P \\vec{E}^*$$
_____________________________________________________________________________________________________________________
_____________________________________________________________________________________________________________________

**Inputs**:
_____________________________________________________________________________________________________________________

`sigmaPed`: Pedersen conductivity in S/m

`Un`: neutral velocity in m/s [ENU]

`B`: magnetic field vector in T [ENU]

`E`: electric field vector in V/m [ENU]

_____________________________________________________________________________________________________________________
_______________________________________________________________________________________________________________________

**Outputs**:
_____________________________________________________________________________________________________________________

`J_ped`: pedersen current density vector in \(A/m^2\)

`J_pede`: pedersen current density in \(A/m^2\) [east]

`J_pedn`: pedersen current density in \(A/m^2\) [north]

`J_pedu`: pedersen current density in \(A/m^2\) [up]

_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

**Reference**:
_____________________________________________________________________________________________________________________

Richmond, A. and Thayer, J. (2000). Ionospheric electrodynamics: A tutorial. Magnetospheric current
systems 118, 131â€“146
______________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

"""


import numpy as np

def current_pedersen(sigmaPed,Un,B,E):

    bnorm = np.sqrt(B[0] * B[0] + B[1] * B[1] + B[2] * B[2])
    b_unit = [B[0] / bnorm, B[1]/ bnorm, B[2] / bnorm]

    Unvert=np.cross(Un,b_unit)

    Estar=E+np.cross(Unvert,B)

    J_ped=sigmaPed*Estar
    J_pe=J_ped[0]
    J_pn=J_ped[1]
    J_pu=J_ped[2]

    return J_ped,J_pe,J_pn,J_pu

"""
**sub_Heating_Sources.N2_rot**

**Description**:
_____________________________________________________________________________________________________________________

Calculate loss due to N2 rotational excitation

$$\\textbf{Schunk and Nagy}$$

$$L_e(N2,rot)= 3.5 \\times 10^{-14} n_e n_{N2} (T_e - T_n) / T_e^{1/2} $$

$$\\textbf{Rees and Roble}$$

$$L_e(N2,rot)= 2.9 \\times 10^{-14} n_e n_{N2} (T_e - T_n) / T_e^{1/2} $$
_____________________________________________________________________________________________________________________
_____________________________________________________________________________________________________________________

**Inputs**:
_____________________________________________________________________________________________________________________

`Necm`: electron density in \(cm^{-3}\)

`NN2`: \(N_2\) density in \(cm^{-3}\)

`Te`: electron temperature in K

`Tn`: neutral temperature in K
_____________________________________________________________________________________________________________________
_______________________________________________________________________________________________________________________

**Outputs**:
_____________________________________________________________________________________________________________________

`Le_N2_rotf_schunk`: Accorsing to Schunk and Nagy [2009] 

`Le_N2_rotf_rees`: According to Rees [1989]

`Le_N2_rotf_tiegcm`: According to TIEGCM formulas
_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

**Reference**:
_____________________________________________________________________________________________________________________

Schunk, R. and Nagy, A. (2009). Ionospheres: physics, plasma physics, and chemistry (Cambridge
university press)

Rees, M. H. (1989). Physics and chemistry of the upper atmosphere. Cambridge University Press.
______________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

"""
import numpy as np

def N2_rot(Necm,NN2,Te,Tn):
    
    #Schunk and Nagy
    Le_N2_rotf_schunk=(3.5*10**(-14)*Necm*NN2*(Te-Tn)/np.sqrt(Te))*1.60217662*10**(-13)  #W/m^3
    #Rees and Roble 
    Le_N2_rotf_rees=(2.9*10**(-14)*Necm*NN2*(Te-Tn)/np.sqrt(Te))*1.60217662*10**(-13)
    #TIEGCM
    Le_N2_rotf_tiegcm=(2.9*10**(-14)*Necm*NN2*(Te-Tn)/np.sqrt(Te))*1.60217662*10**(-13)
    
    
    return Le_N2_rotf_schunk,Le_N2_rotf_rees,Le_N2_rotf_tiegcm
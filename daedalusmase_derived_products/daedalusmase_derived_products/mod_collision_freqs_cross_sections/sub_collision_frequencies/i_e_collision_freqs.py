"""
**sub_heating_sources.i_e_collision_freqs**

**Description**:
_____________________________________________________________________________________________________________________

Calculate ion-electron collision frequencies
_____________________________________________________________________________________________________________________
_____________________________________________________________________________________________________________________

**Inputs**:
_____________________________________________________________________________________________________________________

`NOp`: \(O^+\) density in \(cm^{-3}\)

`NO2p`: \(O_2^+\) density in \(cm^{-3}\)

`NNOp`: \(NO^+\) density in \(cm^{-3}\)

`Nplus`: \(N^+\) density in \(cm^{-3}\)

`Te`: electron temperature in K
_____________________________________________________________________________________________________________________
_______________________________________________________________________________________________________________________

**Outputs**:
_____________________________________________________________________________________________________________________

`ve_i`: electron-ion collision frequency 

`ve_op`: electron- \(O^+\) collision frequency 

`ve_o2p`: electron- \(O_2^+\) collision frequency 

`ve_nop`: electron- \(NO^+\) collision frequency 

`ve_np`: electron- \(N^+\) collision frequency 


_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

**Reference**:
_____________________________________________________________________________________________________________________

Schunk, R. and Nagy, A. (2009). Ionospheres: physics, plasma physics, and chemistry (Cambridge
university press)
______________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

"""

import numpy as np

def i_e_collision_freqs(NOp,NO2p,NNOp,Nplus,Te):
    ve_op=(54.5*NOp)/(Te**(1.5))
    ve_o2p=(54.5*NO2p)/(Te**(1.5))
    ve_nop=(54.5*NNOp)/(Te**(1.5))
    ve_np=(54.5*Nplus)/(Te**(1.5))
    ve_i=(ve_op+ve_o2p+ve_nop+ve_np)/4
    
    return ve_i, ve_op, ve_o2p, ve_nop, ve_np

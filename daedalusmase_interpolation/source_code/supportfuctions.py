def first_order_derivative(f,time,x,y,z,mode):
    """
        Function to calculate First order Derivative
        Args:
            f (float): 4D array of the variable of which derivative is going to calculated
            time (int): time index
            x (int): lev index, as in TIEGCM file
            y (int): latitude index, as in TIEGCM file
            z (int): longitute index, as in TIEGCM file
            mode (String): X,Y or Z according to the dimension of derivation
        Returns:
            retval (float): first order partial derivative or False in case of wrong setup
    """
  
    if mode=="X":
        retval=(f[time,x+1,y,z]-f[time,x-1,y,z])/(2)
        return retval

    if mode=="Y":
        if y>=71 or y<=1: 
            return 0
        retval=(f[time,x,y+1,z]-f[time,x,y-1,z])/(2)
        return retval

    if mode=="Z":
        if z>=143 or z<=1: 
            return 0

        retval=(f[time,x,y,z+1]-f[time,x,y,z-1])/(2)
        return retval

    return False   


def second_order_mixed_derivative(f,time,x,y,z,mode):
    """
        Function to calculate Second Order Mixed Derivative
        Args:
            f (float): 4D array of the variable of which derivative is going to calculated
            time (int): time index
            x (int): lev index, as in TIEGCM file
            y (int): latitude index, as in TIEGCM file
            z (int): longitute index, as in TIEGCM file
            mode (String): XY,XZ or YZ according to the order of derivation
        Returns:
            retval (float): Second order mixed derivative or False in case of wrong setup
    """
 
    if x>=55 or x<=1 or y >=71 or y<=1 or z>=143 or z<=1:
        return 0

    if mode == "XY":

        retval=(f[time,x+1,y+1,z]-f[time,x+1,y-1,z]-f[time,x-1,y+1,z]+f[time,x-1,y-1,z])/(4)
        return retval

    if mode == "XZ":

        retval=(f[time,x+1,y,z+1]-f[time,x+1,y,z-1]-f[time,x-1,y,z+1]+f[time,x-1,y,z-1])/(4)
        return retval

    if mode == "YZ":

        retval=(f[time,x,y+1,z+1]-f[time,x,y+1,z-1]-f[time,x,y-1,z+1]+f[time,x,y-1,z-1])/(4)
        return retval

    return False



def third_order_mixed_derivative(f,time,x,y,z):
    """
        Function to calculate Second Order Mixed Derivative
        Args:
            f (float): 4D array of the variable of which derivative is going to calculated
            time (int): time index
            x (int): lev index, as in TIEGCM file
            y (int): latitude index, as in TIEGCM file
            z (int): longitute index, as in TIEGCM file
        Returns:
            retval (float): Third order mixed derivative or False in case of wrong setup
    """

    if x>=55 or x<=1 or y >=71 or y<=1 or z>=143 or z<=1:
        return 0


    retval=(f[time,x+1,y+1,z+1]-f[time,x+1,y+1,z-1]-f[time,x+1,y-1,z+1]+f[time,x+1,y-1,z-1]-f[time,x-1,y+1,z+1]+\
            f[time,x-1,y+1,z-1]+f[time,x-1,y-1,z+1]-f[time,x-1,y-1,z-1])/(8)

    return retval

def euclidian_distance(p_r,  p_0):
    """
        Function to calculate EuclidianDistace detwwen two points in geodetic coordinates
        Args:
            p_r (list): coordinates of first point in [lat,lon,alt]
            p_0 (list): coordinates of second point in [lat,lon,alt]
        Returns:
            dist (float): distance in km
    """
    lat_1 = (np.pi / 180.0) * (p_r[0])
    lat_2 = (np.pi / 180.0) * (p_0[0])
    lon_1 = (np.pi / 180.0) * (p_r[1])
    lon_2 = (np.pi / 180.0) * (p_0[1])
    alt_1 = p_r[2] + RE
    alt_2 = p_0[2] + RE
    x_1 = 1.0e3 * alt_1 * np.cos(lon_1) * np.cos(lat_1)
    x_2 = 1.0e3 * alt_2 * np.cos(lon_2) * np.cos(lat_2)
    y_1 = 1.0e3 * alt_1 * np.sin(lon_1) * np.cos(lat_1)
    y_2 = 1.0e3 * alt_2 * np.sin(lon_2) * np.cos(lat_2)
    z_1 = 1.0e3 * alt_1 * np.sin(lat_1)
    z_2 = 1.0e3 * alt_2 * np.sin(lat_2)
    dist = np.sqrt((x_2 - x_1) * (x_2 - x_1) + (y_2 - y_1) * (y_2 - y_1) + (z_2 - z_1) * (z_2 - z_1))
    return dist


